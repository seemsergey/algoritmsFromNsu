#include <stdio.h>

#define V 6          // Количество вершин (A, B, C, D, E, F)
#define E_COUNT 9    // Количество ребер
#define INF 99999    // Значение бесконечности (избегаем INT_MAX, чтобы не было переполнения при сложении)

// Структура для хранения ребра
struct Edge {
    int from; // откуда
    int to; // куда
    int w; // вес
};

int main() {
    // Массив имен вершин для красивого вывода
    char names[] = {'A', 'B', 'C', 'D', 'E', 'F'};

    // Индексы: A=0, B=1, C=2, D=3, E=4, F=5
    struct Edge edges[E_COUNT] = {
        {0, 1, 2},  // A -> B, вес 2
        {0, 2, 3},  // A -> C, вес 3
        {1, 2, 1},  // B -> C, вес 1
        {1, 3, 3},  // B -> D, вес 3
        {1, 4, 5},  // B -> E, вес 5
        {2, 3, 4},  // C -> D, вес 4
        {3, 5, 2},  // D -> F, вес 2
        {5, 2, -5}, // F -> C, вес -4 (отрицательное ребро)
        {5, 4, 6}   // F -> E, вес 6
    };

    // d[вершина][шаг k]
    int d[V][V];
    // p[вершина][шаг k] - для хранения предка (откуда пришли)
    int p[V][V];

    // 1. Инициализация таблицы
    for (int v = 0; v < V; v++) {
        for (int k = 0; k < V; k++) {
            d[v][k] = INF;
            p[v][k] = -1; // -1 означает нет предка
        }
    }

    // Стартовая вершина A (индекс 0)
    d[0][0] = 0;

    // 2. Основной цикл алгоритма (k - количество ребер)
    for (int k = 1; k < V; k++) {
        // Сначала переносим значения с предыдущего шага (d[j][k-1])
        for (int v = 0; v < V; v++) {
            d[v][k] = d[v][k-1];
            p[v][k] = p[v][k-1];
        }

        // Релаксация всех ребер
        for (int i = 0; i < E_COUNT; i++) {
            int u = edges[i].from;
            int v = edges[i].to;
            int w = edges[i].w;

            // Если путь до вершины 'u' на шаге k-1 существует
            // и новый путь через 'u' короче текущего пути до 'v' на шаге k
            if (d[u][k-1] != INF && d[u][k-1] + w < d[v][k]) {
                d[v][k] = d[u][k-1] + w;
                p[v][k] = u; // запоминаем предка
            }
        }
    }

    // 3. Вывод таблицы
    printf("   k|");
    for (int k = 0; k < V; k++) {
        printf("   %d   |", k);
    }
    printf("\n----+-------+-------+-------+-------+-------+-------+\n");

    for (int v = 0; v < V; v++) {
        printf(" %c  |", names[v]);
        for (int k = 0; k < V; k++) {
            if (d[v][k] == INF) {
                printf("  inf  |");
            } else if (p[v][k] == -1) {
                // Если предка нет (это стартовая вершина)
                printf("   %d   |", d[v][k]);
            } else {
                // Выводим "расстояние, предок" как на доске
                printf(" %2d, %c |", d[v][k], names[p[v][k]]);
            }
        }
        printf("\n");
    }

    return 0;
}